import re


def gen_j_dict():
    equations = re.findall(r'[^\s]+\s+=\s+\w+', TEXT)
    new_text = ''
    for e in equations:
        new_text += e + "\n"
    with open('jp.data', 'wb+') as f:
        f.write(new_text[:-1].encode('utf8'))


